    private void CleanDisplay()
    {
        jTextFieldDisplay.setText("");
        jTextFieldHistoryDisplay.setText("");
        aux = "";
        result = 0.0;
    }
    
    private void SetAux(String symbol)
    {
        aux = aux + symbol;
        jTextFieldDisplay.setText(aux);
    }
    
    private String PrecisionConverter(int index)
    {
        String resultString = "";
        switch(index)
        {
            case 0:
                resultString = String.format("%s = %.0f", (aux), (result));
                break;
            case 1:
                resultString = String.format("%s = %f", (aux), (result));
                break;
            case 2:
                resultString = String.format("%s = %.2f", (aux), (result));
                break;
        }
        return resultString;
    }
    
    
    
    
    

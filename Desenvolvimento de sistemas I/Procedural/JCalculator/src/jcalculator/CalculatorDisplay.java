    private void jButtonEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEqualsActionPerformed
        //função que converte String em uma operação matemática, utilizando o expr
        Expr expr;
        try {
            expr = Parser.parse(aux);
            result = expr.value();
        } catch(SyntaxException e) {
            System.err.println(e.explain());
        }
        jTextFieldHistoryDisplay.setText(PrecisionConverter(jComboBoxOutP.getSelectedIndex()));
        
        aux = String.valueOf(result);
        jTextFieldDisplay.setText(aux);
        result = 0.0;
    }//GEN-LAST:event_jButtonEqualsActionPerformed

